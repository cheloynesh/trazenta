function nuevoMovimiento(id)
{
    idupdate=id;
    var table = $('#tbProf1').DataTable();
    var route = baseUrl + '/GetInfo/'+ id;

    jQuery.ajax({
        url:route,
        type:'get',
        dataType:'json',
        success:function(result)
        {
            table.clear();
            console.log(result.data)
            result.data.forEach( function(valor, indice, array) {
                table.row.add([valor.apply_date,valor.currency,valor.amount,valor.type]).node().id = valor.id;
            });
            table.draw(false);
        }
    })
    $("#myModal2").modal('show');
}
function cancelarMovimiento()
{
    $("#myModal2").modal('hide');
}
